          						¿Por qué debo registrar la colección biológica?
        					</a>
      					</h4>
    				</div>
    				<div id="collapseTwo" class="panel-collapse collapse" role="tabpanel" aria-labelledby="headingTwo">
      					<div class="panel-body">
        					<p>Dada la importancia que tienen las colecciones biológicas como depositarias del patrimonio natural del país, es necesario visibilizarlas y fortalecerlas. Uno de los instrumentos que busca disponer información en línea sobre las colecciones biológicas para contribuir a su fortalecimiento es el Registro Único Nacional de Colecciones Biológicas, disponible en la página web:<p>
        					<a href="http://rnc.humboldt.org.co/index.php/registros/colecciones">http://rnc.humboldt.org.co/index.php/registros/colecciones</a>
        					<p>Este instrumento es administrado por el Instituto Humboldt, según lo establecido en el artículo 2.2.2.9.1.3 del Decreto 1076 de 2015 y de acuerdo con el artículo 2.2.2.9.1.5 es obligación de la persona natural o jurídica que administre una colección biológica realizar el Registro Único de la Colección Biológica ante el Instituto de Investigación de Recursos Biológicos Alexander von Humboldt.<p>
      					</div>
