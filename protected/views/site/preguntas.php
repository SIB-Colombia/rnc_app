							<p>Verifique previamente si la institución o persona que será titular de la colección se encuentra registrada (vea también: <a href="#headingThree">¿Quién es el titular de la colección?</a>). Para esto puede realizar una búsqueda rapida en: <a href="http://rnc.humboldt.org.co/index.php/registros/colecciones">http://rnc.humboldt.org.co/index.php/registros/colecciones</a> en el campo “Titular de la colección”.</p>

							<p>En caso que la institución esté registrada póngase en contacto con el representante legal quien cuenta con las credenciales de acceso.</p>

							<p>En caso de no estar registrado diligencie y envíe el formulario de solicitud de usuario y contraseña en: <a href="http://rnc.humboldt.org.co/index.php/entidad/solicitud">http://rnc.humboldt.org.co/index.php/entidad/solicitud.</a></p>

							<p>Ingrese al aplicativo con las credenciales asignadas (<a href="http://rnc.humboldt.org.co/index.php/admin/login">http://rnc.humboldt.org.co/index.php/admin/login</a>) y diligencie el Formulario de registro. Lea atentamente la guía para su correcto diligenciamiento y así evitar demoras en el trámite de la solicitud.</p>

							<p>Anexe al formulario en línea los siguientes documentos escaneados:</p>
							<ul>
								<li>Documento por el cual se crea la colección biológica por parte del titular, donde conste: el nombre de la colección, la fecha de inicio de actividades como colección, el objeto y los recursos destinados actualmente por el titular para el sostenimiento de la colección.</li>
								<li>Certificado de existencia y representación legal (no mayor a 3 meses).</li>
								<li>Cédula de ciudadanía del representante legal.</li>
								<li>Formato de autodeclaración firmado por el titular de la colección disponible en la página web <a href="http://rnc.humboldt.org.co/index.php/site/instructivo">http://rnc.humboldt.org.co/index.php/site/instructivo</a> (no mayor a 3 meses de expedición).</li>
								<li>Documentos que acrediten la obtención legal de los especímenes.</li>
								<li>Protocolo de manejo de la colección.</li>
							</ul>
							<p>
								Envíe el formulario y en un plazo de 20 días hábiles, si no existen observaciones, será enviado un correo electrónico del contacto de la colección anexando la certificación de registro, la cual también será enviada en físico al titular de la colección.
