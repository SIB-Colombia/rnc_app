    				<div class="panel-heading" role="tab" id="headingThree">
      					<h4 class="panel-title">
        					<a class="collapsed" role="button" data-toggle="collapse" data-parent="#accordion" href="#collapseThree" aria-expanded="false" aria-controls="collapseThree">
          						¿Qué tipo de colecciones se registran en el Registro Único Nacional de Colecciones Biológicas?
        					</a>
      					</h4>
    				</div>
    				<div id="collapseThree" class="panel-collapse collapse" role="tabpanel" aria-labelledby="headingThree">
      					<div class="panel-body">
        					<p>El RNC facilita la consolidación de datos e información sobre las colecciones biológicas, contribuyendo a promover su acceso, uso y el diseño de estrategias de fortalecimiento.</p>
							<p>Las colecciones biológicas registradas en el RNC según lo establecido en el Decreto 1076 de 2015 y el concepto emitido por el Ministerio de Ambiente y Desarrollo Sostenible del 6 de octubre de 2015, serán:</p>
							<ul>
								<li>Colecciones biológicas de especímenes preservados de especies silvestres nativas.</li>
								<li>Colecciones de microorganismos provenientes de especies domésticas y humanos.</li>
								<li>Colecciones biológicas de especies domésticas, cultivadas o exóticas.</li>
								<li>Estaciones biológicas como serpentarios, aviarios, ofidiarios, insectarios, y similares.</li>
								<li>Colecciones vivas de zoológicos, acuarios y jardines botánicos.</li>
							</ul>
							<p>No se consideran en el ámbito de registro del RNC:</p>
							<ul>
								<li>Colecciones arqueológicas: de acuerdo con la Ley 1185 de 2008 serán registradas por el Instituto Colombiano de Antropología e Historia (ICANH).</li>
								<li>Colecciones paleontológicas: conforme al Decreto 2703 de 2013 serán registradas por el Servicio Geológico Colombiano-SGC.</li>
							</ul>
							<p>Las colecciones biológicas estarán sujetas al concepto de viabilidad de registro en el RNC expedido por el Instituto Humboldt.</p>
