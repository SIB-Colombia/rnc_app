          						¿Qué tipo de actividades puede realizar una colección biológica?
        					</a>
      					</h4>
    				</div>
    				<div id="collapseSix" class="panel-collapse collapse" role="tabpanel" aria-labelledby="headingSix">
      					<div class="panel-body">
        					<p>Según el artículo 2.2.2.9.1.4 del Decreto 1076 de 2015 las colecciones biológicas además de ser receptoras de especímenes y de adelantar actividades de curaduría para garantizar el mantenimiento y cuidado de estos podrán adelantar, entre otras:</p>
        					<ul>
        						<li>Actividades con fines científicos, orientadas de manera exclusiva a generar conocimiento e información científica básica, con el fin de descubrir y explicar fenómenos y procesos naturales, sin que incluyan actividades de prospección biológica, aplicación industrial o aprovechamiento comercial.</li>
								<li>Labores educativas y divulgativas sobre la biodiversidad nacional.</li>
								<li>Apoyo a la implementación de programas de conservación.</li>
								<li>Proveer especímenes para el desarrollo de investigaciones.</li>
								<li>Prestar e intercambiar especímenes con otras colecciones biológicas nacionales o internacionales.</li>
        					</ul>
