      					</h4>
    				</div>
    				<div id="collapseFive" class="panel-collapse collapse" role="tabpanel" aria-labelledby="headingFive">
      					<div class="panel-body">
        					<p>La obtención legal de los especímenes se puede acreditar por los siguientes documentos:</p>
        					<ul>
        						<li>Permiso de caza científica (Decreto 1608 de 1978) (Fauna)</li>
								<li>Licencia científica de flora (Acuerdo 38 de 1973) (Flora)</li>
								<li>Permiso de investigación (Decreto 309 de 2000)</li>
								<li>Permiso de recolección de especímenes de especies silvestres (Decreto 1376 y Decreto 3016 de 2013)</li>
								<li>Contratos de acceso a recursos genéticos (Decreto 1376 de 2013)</li>
								<li>Documento de depósito temporal o definitivo derivados de incautaciones, decomisos u otros, emitidos por autoridades ambientales.</li>
								<li>Documento de depósito realizado por institutos de investigación adscritos o vinculados al Ministerio de Ambiente o Desarrollo Sostenible.</li>
								<li>Certificados de donación de otras organizaciones de especímenes a la colección. En este caso se requiere el permiso de obtención legal de los especímenes por parte de quienes realizan la donación.</li>
        					</ul>
