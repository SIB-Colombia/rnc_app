        					<a class="collapsed" role="button" data-toggle="collapse" data-parent="#accordion" href="#collapseEight" aria-expanded="false" aria-controls="collapseEight">
          						¿Qué obligaciones tienen las colecciones biológicas?
        					</a>
      					</h4>
    				</div>
    				<div id="collapseEight" class="panel-collapse collapse" role="tabpanel" aria-labelledby="headingEight">
      					<div class="panel-body">
        					<p>Las colecciones biológicas deberán cumplir con las siguientes obligaciones según el Decreto 1076 de 2015 Artículo 2.2.2.9.1.6 y la Resolución 1115 de 2000:</p>
        					<ul>
        						<li>Registrarse en el Registro Único Nacional de Colecciones Biológicas (RNC).</li>
								<li>Mantener actualizada la información de la colección biológica en el RNC. La actualización debe realizarse mínimo cada dos (2) años.</li>
								<li>Mantener actualizada y compartir, bajo previo acuerdo, la información asociada a los especímenes depositados en las colecciones, con el Sistema de Información sobre Biodiversidad de Colombia (SiB Colombia).</li>
								<li>Remitir en forma digital al Ministerio de Ambiente y Desarrollo Sostenible, las publicaciones derivadas de las investigaciones adelantadas con especímenes de esta misma.</li>
								<li>Dar cumplimiento protocolo de manejo de la colección.</li>
								<li>Elaborar y actualizado un reglamento interno para el uso cada colección y hacerlo disponible a interesados.</li>
								<li>Recepcionar los especímenes (cuando así se considere) con los soportes que acrediten la respectiva obtención legal. </li>
	       					</ul>
