          						¿Qué derechos tienen las colecciones biológicas?
        					</a>
      					</h4>
    				</div>
    				<div id="collapseSeven" class="panel-collapse collapse" role="tabpanel" aria-labelledby="headingSeven">
      					<div class="panel-body">
        					<p>Las colecciones biológicas registradas en el Registro Único Nacional de Colecciones Biológicas, son consideradas Institución Nacional de Investigación y por tanto pueden solicitar permisos marco de recolección (Decreto 1076 de 2015, Artículo 2.2.2.8.1.3).</p>

							<p>Las colecciones biológicas pueden realizar investigación básica sin fines comerciales en los temas de sistemática molecular, ecología molecular, evolución y biogeografía molecular sin que ello configure acceso a recursos genéticos (Decreto 1076 de 2015, Artículo 2.2.2.9.1.4 Parágrafo 1).</p>

							<p>Para la movilización de especímenes en el territorio nacional, provenientes de colecciones registradas en el Registro Único Nacional de Colecciones Biológicas no se requiere salvoconducto; actúa como tal la constancia de registro o actualización de la colección  y una certificación del titular donde consten los ejemplares movilizados. (Decreto 1076 de 2015, Artículo 2.2.2.9.1.9)</p>

							<p>Lo anterior sin perjuicio de la normativa aplicable a zoológicos, acuarios y jardines botánicos, así como de las disposiciones sobre bioseguridad, salud pública, sanidad animal y vegetal vigentes. (Decreto 1076 de 2015 artículo 2.2.2.9.1.2) . Por lo tanto, la movilización de especímenes vivos, elaboración de planes de manejo, permisos de recolección, entre otros, que realicen los zoológicos, acuarios, jardines botánicos y similares serán objeto de definición y seguimiento por la autoridad ambiental competente.</p>
