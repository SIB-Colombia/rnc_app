      					<h4 class="panel-title">
        					<a class="collapsed" role="button" data-toggle="collapse" data-parent="#accordion" href="#collapseNteen" aria-expanded="false" aria-controls="collapseNteen">
          						¿Qué debo hacer para prestar e intercambiar ejemplares a una colección en el extranjero?
        					</a>
      					</h4>
    				</div>
    				<div id="collapseNteen" class="panel-collapse collapse" role="tabpanel" aria-labelledby="headingNteen">
      					<div class="panel-body">
        					<p>Las colecciones biológicas podrán prestar e intercambiar especímenes con otras colecciones biológicas nacionales o internacionales. En esos casos se deberán suscribir convenios o contratos y mantener un libro de registro de los préstamos e intercambios, los cuales podrán ser consultados en cualquier momento por la autoridad ambiental competente en ejercicio de su función de control y vigilancia. Los holotipos de las colecciones biológicas únicamente podrán salir del país en calidad de préstamo. (Decreto 1076 de 2015 artículo 2.2.2.9.1.4)</p>

							<p>Los interesados en la importación y exportación de ejemplares de colecciones deberán obtener la autorización de importación de especímenes o muestras de la diversidad biológica con fines de investigación científica por parte del Ministerio de Ambiente y Desarrollo Sostenible, sin perjuicio de lo dispuesto por el artículo 52, numeral 12 de la Ley 99 de 1993. Así mismo el permiso de exportación relacionado con la Convención CITES o con las disposiciones para especies NO CITES, según el caso. (Decreto 1076 de 2015 artículos 2.2.1.6.1.1 y 2.2.2.9.1.10)</p>

							<p>En cualquier caso para el estudio de la importación o exportación se deberá documentar la obtención legal de los especímenes del país de origen. Para consultar si las especies a importar/exportar se encuentra bajo la convencion CITES visite la web: <a href="https://www.cites.org/esp/app/appendices.php">https://www.cites.org/esp/app/appendices.php</a> </p>

							<p>La entidad competente para otorgar las autorizaciones relacionadas con especies NO CITES es la Autoridad Nacional de Licencias Ambientales ANLA. Para mayor información sobre el trámite, formatos y costos contacte al correo electrónico licencias@anla.gov.co o consulte la página web: <a href="http://www.anla.gov.co/descripcion-tramites">http://www.anla.gov.co/descripcion-tramites</a></p>

							<p>La entidad competente para otorgar los permisos CITES es el Ministerio de Ambiente y Desarrollo Sostenible. Para mayor información sobre el trámite, formatos y costos contacte al correo electrónico servicioalciudadano@minambiente.gov.co o consulte la página web: <a href="https://www.sivirtual.gov.co/memoficha-tramite/-/tramite/T1753">https://www.sivirtual.gov.co/memoficha-tramite/-/tramite/T1753</a></p>
