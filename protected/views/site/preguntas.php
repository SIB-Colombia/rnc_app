        					<a class="collapsed" role="button" data-toggle="collapse" data-parent="#accordion" href="#collapseSvteen" aria-expanded="false" aria-controls="collapseSvteen">
          						¿Cómo puedo hacer pública la información asociada a los ejemplares en el SiB Colombia?
        					</a>
      					</h4>
    				</div>
    				<div id="collapseSvteen" class="panel-collapse collapse" role="tabpanel" aria-labelledby="headingSvteen">
      					<div class="panel-body">
        					<p>Una de las obligaciones de las colecciones biológicas según el Artículo 2.2.2.9.1.6 de Decreto 1076 de 2015 es “Mantener actualizada y compartir, bajo previo acuerdo, la información asociada a los especímenes depositados en las colecciones, con el Sistema de Información sobre Biodiversidad de Colombia (SiB Colombia)”.</p>

							<p>El proceso de publicación de la información asociada a los ejemplares como: taxonomía al mejor nivel de detalle posible, localidad (incluyendo altitud y coordenadas geográficas); fecha de recolecta y colector, entre otras; debe realizarse a través del procedimiento establecido por el Sistema de Información sobre Biodiversidad de Colombia (SiB Colombia) disponible en la página web: <a href="http://www.sibcolombia.net">http://www.sibcolombia.net</a></p>
      					</div>
    				</div>
  				</div>
  				<div class="panel panel-default">
  					<div class="panel-heading" role="tab" id="headingEteen">
      					<h4 class="panel-title">
        					<a class="collapsed" role="button" data-toggle="collapse" data-parent="#accordion" href="#collapseEteen" aria-expanded="false" aria-controls="collapseEteen">
          						¿Los ejemplares se legalizan al incluirse como parte de una colección registrada en el Registro Único Nacional de Colecciones Biológicas, al publicar los datos a través del SiB Colombia o al reportarlos en el Certificado de Reporte a través del SiB Colombia (CR-SiB)?
        					</a>
      					</h4>
    				</div>
    				<div id="collapseEteen" class="panel-collapse collapse" role="tabpanel" aria-labelledby="headingEteen">
      					<div class="panel-body">
        					<p>No, los ejemplares que se depositan en una colección biológica registrada deben tener previamente el soporte  de su obtención legal. Según el Decreto 1076 de 2015 artículo 2.2.2.9.1.8, Depósito de los Especímenes:</p>
        					<ul>
        						<li>Las colecciones biológicas registradas <b>recibirán los especímenes legalmente colectados</b> que cumplan con estándares adecuados de calidad y curaduría, y que se encuentren conforme a las normas de ingreso propias para cada colección. [...]</li>
								<li>La colección biológica registrada podrá reservarse el derecho de recibir  especímenes de la diversidad biológica por las siguientes razones: No contar con el permiso de recolección de especímenes silvestres de la diversidad biológica con fines de investigación científica no comercial, o permiso de recolección de especímenes de la diversidad biológica para elaboración de estudios ambientales u otra documentación que acredite su procedencia legal.</li>
        					</ul>
        					<p>El reporte de los ejemplares depositados en la colección a través del aplicativo del RNC en línea, y publicación de los datos asociados en el SiB, son obligaciones de las colecciones biológicas (Ver <a href="#headingSeven">¿Qué obligaciones tienen las colecciones biológicas?).</a></p>

							<p>El reporte en el aplicativo para generación de Certificado de Reporte CR-SiB es una obligación adquirida con la autoridad ambiental que otorga los permisos de recolección (Ver <a href="headingTwnine">¿Como puedo obtener el certificado de reporte a través del SiB Colombia (CR-SiB)?</a>).</p>

							<p>Ninguno de estos reportes ampara o legaliza ejemplares obtenidos sin los respectivos permisos u otra documentación (Ver <a href="#headingFour">¿Qué documentos acreditan la obtención legal de los especímenes?</a>).</p>
