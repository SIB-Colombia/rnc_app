    				</div>
    				<div id="collapseFour" class="panel-collapse collapse" role="tabpanel" aria-labelledby="headingFour">
      					<div class="panel-body">
        					<p>El titular de la colección es la persona que registra la colección, y es jurídica responsable de la misma. En el caso de personas jurídicas el titular es el representante legal o quien haga sus veces.</p>
        					<p>En la mayoría de los casos las colecciones son dependientes de instituciones de educación superior, centros de investigación o fundaciones. Aunque las colecciones son manejadas por curadores, investigadores y docentes, es el representante legal de la entidad quien actuará como titular y es el responsable jurídico del patrimonio nacional depositado en las colecciones registradas a su nombre.</p>
        					<p>Para temas técnicos existirá un contacto de la colección que será visible para el público en la mayoría de los casos corresponde al curador o director de la colección.</p>

