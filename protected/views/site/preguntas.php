      					<div class="panel-body">
        					<p>Una colección biológica se define según el Decreto 1076 de 2015 artículo 2.2.2.9.1.3  como:</p>
        					<p>
        					“Conjunto de especímenes de la diversidad biológica preservados bajo estándares de curaduría especializada para cada uno de los grupos depositados en ella, los cuales deben estar debidamente catalogados, mantenidos y organizados taxonómicamente, de conformidad con lo establecido en el protocolo de manejo respectivo, que constituyen patrimonio de la Nación y que se encuentran bajo la administración de una persona natural o jurídica, tales como herbarios, museos de historia natural, bancos de germoplasma, bancos de tejidos y ADN, genotecas y ceparios y las demás que el Ministerio de Ambiente y Desarrollo Sostenible así lo considere.”</p>
